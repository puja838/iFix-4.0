package handlers

import (
	"encoding/json"
	"iFIX/ifix/entities"
	"iFIX/ifix/logger"
	"iFIX/ifix/models"
	"log"
	"net/http"
)

func ThrowReportDownloadListIntResponse(successMessage string, responseData int64, w http.ResponseWriter, success bool) {
	var response = entities.ReportDownloadResponseInt{}
	response.Success = success
	response.Message = successMessage
	response.Details = responseData
	jsonResponse, jsonError := json.Marshal(response)
	if jsonError != nil {
		logger.Log.Fatal("Internel Server Error")
	}
	w.Header().Set("Content-Type", "application/json")
	w.Write(jsonResponse)
}

func ReportDownloadList(w http.ResponseWriter, req *http.Request) {
	var data = entities.ReportDownloadEntity{}
	jsonError := data.FromJSON(req.Body)
	if jsonError != nil {
		log.Print(jsonError)
		logger.Log.Println(jsonError)
		entities.ThrowJSONResponse(entities.JSONParseErrorResponse(), w)
	} else {
		data, success, _, msg := models.ReportDownloadList(&data)
		ThrowReportDownloadListIntResponse(msg, data, w, success)
	}
}
